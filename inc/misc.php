/** Add site-wide JavaScripts */
function dpe_baseline_javascripts() {
	// Load jQuery from Google instead of the internal WP version
	wp_deregister_script( 'jquery' );
	wp_register_script( 'jquery', 'http://ajax.googleapis.com/ajax/libs/jquery/1.7.1/jquery.min.js', NULL, NULL, true);
	wp_enqueue_script( 'jquery' );
	//wp_enqueue_script( 'dpe-baseline-plugins', get_bloginfo('stylesheet_directory') . '/js/plugins.js', array( 'jquery' ), NULL, true );
	wp_enqueue_script( 'dpe-baseline-default', get_bloginfo('stylesheet_directory') . '/js/default.js', array( 'jquery' ), NULL, true );
}
add_action( 'wp_enqueue_scripts', 'dpe_baseline_javascripts' );

/**
 * Disable WordPress a number of WordPress generated header links
 */
function dpe_remove_wp_head_links() {
	remove_action( 'wp_head', 'start_post_rel_link' );
	remove_action( 'wp_head', 'adjacent_posts_rel_link_wp_head' );
	remove_action( 'wp_head', 'wp_generator' );
	remove_action( 'wp_head', 'rsd_link' );
	remove_action( 'wp_head', 'wlwmanifest_link' );
	remove_action( 'wp_head', 'parent_post_rel_link' );
}
add_action('init', 'dpe_remove_wp_head_links');

